
def sum(a, b):
    c = a + b
    print("{} + {} = {}".format(a, b, c))

def sub(a, b):
    c = a - b
    print("{} - {} = {}".format(a, b, c))

def mul(a, b):
    c = a * b
    print("{} * {} = {}".format(a, b, c))

def div(a, b):
    c = a / b
    print("{} / {} = {}".format(a, b, c))

sum(10, 5)
sub(10, 5)
mul(10, 5)
div(10, 5)
